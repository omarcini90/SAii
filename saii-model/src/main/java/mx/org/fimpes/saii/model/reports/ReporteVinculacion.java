public class ReporteVinculacion {
	private Integer idInstitucion;
	private String nombreInstitucion;
	private Integer idCampus;
	private String nombre;
	private String entidadFederativa;
	private Integer conveniosPublicos;
	private Integer conveniosPrivados;
	private Integer conveniosSociales;
	private Integer actividadesVinculacion;
	private Integer personasBeneficiadas;
	private Integer proyectosIncubadoras;
	private Integer anio;
	
	public Integer getIdInstitucion() {
		return idInstitucion;
	}
	public void setIdInstitucion(Integer idInstitucion) {
		this.idInstitucion = idInstitucion;
	}
	public String getNombreInstitucion() {
		return nombreInstitucion;
	}
	public void setNombreInstitucion(String nombreInstitucion) {
		this.nombreInstitucion = nombreInstitucion;
	}
	public Integer getIdCampus() {
		return idCampus;
	}
	public void setIdCampus(Integer idCampus) {
		this.idCampus = idCampus;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getEntidadFederativa() {
		return entidadFederativa;
	}
	public void setEntidadFederativa(String entidadFederativa) {
		this.entidadFederativa = entidadFederativa;
	}
	public Integer getConveniosPublicos() {
		return conveniosPublicos;
	}
	public void setConveniosPublicos(Integer conveniosPublicos) {
		this.conveniosPublicos = conveniosPublicos;
	}
	public Integer getConveniosPrivados() {
		return conveniosPrivados;
	}
	public void setConveniosPrivados(Integer conveniosPrivados) {
		this.conveniosPrivados = conveniosPrivados;
	}
	public Integer getConveniosSociales() {
		return conveniosSociales;
	}
	public void setConveniosSociales(Integer conveniosSociales) {
		this.conveniosSociales = conveniosSociales;
	}
	public Integer getActividadesVinculacion() {
		return actividadesVinculacion;
	}
	public void setActividadesVinculacion(Integer actividadesVinculacion) {
		this.actividadesVinculacion = actividadesVinculacion;
	}
	public Integer getPersonasBeneficiadas() {
		return personasBeneficiadas;
	}
	public void setPersonasBeneficiadas(Integer personasBeneficiadas) {
		this.personasBeneficiadas = personasBeneficiadas;
	}
	public Integer getProyectosIncubadoras() {
		return proyectosIncubadoras;
	}
	public void setProyectosIncubadoras(Integer proyectosIncubadoras) {
		this.proyectosIncubadoras = proyectosIncubadoras;
	}
	public Integer getAnio() {
		return anio;
	}
	public void setAnio(Integer anio) {
		this.anio = anio;
	}
}
